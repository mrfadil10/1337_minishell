#include "../minishell.h"

int	ft_lstsize2(t_data *lst)
{
	int		a;

	a = 0;
	if (!lst)
		return (0);
	while (lst != 0)
	{
		lst = lst->next;
		a++;
	}
	return ((a + 1) / 2);
}
